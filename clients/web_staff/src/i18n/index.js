const lang = ref(localStorage.getItem(STORAGE_KEY) || DEFAULT_LANG)

const formatMsg = (value, params = {}) => {
  if (!value) return ''
  return value.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? ''))
}

export const useI18n = () => {
  const t = (key, params = {}) => {
    const table = messages[lang.value] || messages[DEFAULT_LANG]
    const raw = table[key] ?? messages[DEFAULT_LANG][key] ?? key
    return formatMsg(raw, params)
  }

  const setLang = (value) => {
    lang.value = value === 'en-US' ? 'en-US' : 'zh-CN'
    localStorage.setItem(STORAGE_KEY, lang.value)
  }

  return { lang, setLang, t }
}

